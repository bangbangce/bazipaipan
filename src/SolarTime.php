    /**
     * 计算真太阳时
     * 
     * 真太阳时 = 平太阳时 + 时差 + 经度修正
     * 
     * 算法说明：
     * 1. 时差(Equation of Time)：由于地球公转轨道是椭圆的，真太阳时与平太阳时每天会有差异
     *    使用近似公式：EOT = 9.87*sin(2B) - 7.53*cos(B) - 1.5*sin(B)
     *    其中 B = 2π*(n-81)/365，n为一年中的第几天
     * 
     * 2. 经度修正：中国使用东经120度作为标准时间（北京时间）
     *    经度每差1度，时间差4分钟
     *    经度修正 = (120 - 当地经度) * 4 分钟
     * 
     * 3. 总修正量 = 时差 + 经度修正（单位：分钟）
     * 
     * @param int $year 年份
     * @param int $month 月份（1-12）
     * @param int $day 日期（1-31）
     * @param int $hour 小时（0-23）
     * @param int $minute 分钟（0-59）
     * @param int $second 秒（0-59）
     * @param float $longitude 经度（东经为正）
     * @return array 返回真太阳时信息数组，包含：
     *               - year: 调整后的年份
     *               - month: 调整后的月份
     *               - day: 调整后的日期
     *               - hour: 真太阳时小时
     *               - minute: 真太阳时分钟
     *               - second: 真太阳时秒
     *               - correction: 总修正量（分钟）
     *               - eot: 时差（分钟）
     *               - longitudeCorrection: 经度修正（分钟）
     */
    public static function calcSolarTime($year, $month, $day, $hour, $minute, $second, $longitude)
    {
        // 创建日期对象
        $date = new \DateTime(sprintf('%04d-%02d-%02d', $year, $month, $day));
        
        // 获取一年中的第几天（0-365），加1变为（1-366）
        $dayOfYear = (int)$date->format('z') + 1;

        // 计算时差（Equation of Time）
        // B = 2π*(n-81)/365，其中81是因为春分大约在第81天
        $b = 2 * M_PI * ($dayOfYear - 81) / 365;
        
        // 时差公式（单位：分钟）
        $eot = 9.87 * sin(2 * $b) - 7.53 * cos($b) - 1.5 * sin($b);

        // 中国标准时间子午线：东经120度
        $standardMeridian = 120;
        
        // 计算经度修正（单位：分钟）
        // 经度每差1度，时间差4分钟
        $longitudeCorrection = ($standardMeridian - $longitude) * 4;

        // 计算总修正量（单位：分钟）
        $totalCorrection = $eot + $longitudeCorrection;

        // 将北京时间转换为总秒数，并应用修正
        $totalSeconds = $hour * 3600 + $minute * 60 + $second + $totalCorrection * 60;

        // 计算真太阳时的时、分、秒
        $solarHour = (int)($totalSeconds / 3600);
        $remainingSeconds = $totalSeconds % 3600;
        $solarMinute = (int)($remainingSeconds / 60);
        $solarSecond = $remainingSeconds % 60;

        // 处理跨日情况
        if ($solarHour < 0) {
            // 如果小时为负数，说明是前一天
            $solarHour += 24;
            $date->modify('-1 day');
        } elseif ($solarHour >= 24) {
            // 如果小时超过24，说明是后一天
            $solarHour -= 24;
            $date->modify('+1 day');
        }

        return [
            'year' => (int)$date->format('Y'),
            'month' => (int)$date->format('m'),
            'day' => (int)$date->format('d'),
            'hour' => $solarHour,
            'minute' => $solarMinute,
            'second' => abs($solarSecond),
            'correction' => $totalCorrection,
            'eot' => $eot,
            'longitudeCorrection' => $longitudeCorrection,
        ];
    }
